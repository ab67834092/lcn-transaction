    @Override
    public void commit() throws SQLException {
        System.out.println("commit");
        //单独开线程等待
        new Thread(new Runnable() {
            @Override
            public void run() {
                cjbTransaction.getTask().waitTask();
                try {
                    //修改事务的状态应该交由事务管理器的客户端来改
                    if(cjbTransaction.getTransactionType().equals(TransactionType.commit)){
                        connection.commit();
                    }else{
                        connection.rollback();
                    }
                    connection.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
        }).start();
    }

    @Override
    public void rollback() throws SQLException {
        System.out.println("rollback");
        new Thread(new Runnable() {
            @Override
            public void run() {
                cjbTransaction.getTask().waitTask();
                try {
                    connection.rollback();
                    connection.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
        }).start();
    }

